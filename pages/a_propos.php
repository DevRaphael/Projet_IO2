<ul><li><strong>IO2 - Internet et Outils</strong></li></ul>
<p>Ce site web a été réalisé dans le cadre d'un projet d'étude en licence Informatique à 
l'<a href="http://www.univ-paris-diderot.fr">Université Paris Diderot</a>.</p>
<p>Le thème de ce projet portait sur les associations étudiantes. Il nous a été demandé de concevoir 
le site web dynamique d'une association de notre choix où quiconque pourrait s'inscrire et ainsi participer à la vie du site et à sa communauté.</p>

<ul><li><strong>Réalisation du site</strong></li></ul>

<p>La réalisation du site a demandé l'utilisation de HTML, CSS, PHP, MySQL (avec l'extension PDO) et même un peu de JavaScript.
La charte graphique principale a été pensée pour une résolution de 1024 pixels de large, mais devrait plutôt bien 
s'adapter aux autres tailles grâce à la mise en place de <span class="italique">medias queries</span>.</p>

<p>Assurer la compatibilité avec les anciennes version de IE, 6 7 et 8 s'est révélé en revanche trop fastidieux et sans résultat convaincant, l'idée
 a donc été abandonnée. Il est vivement recommandé de découvrir les différentes fonctionnalités ce site avec un navigateur récent (et décent).</p>

 <p>L'architecture du site devrait être à peu près cohérente même si aucun <span class="italique">design pattern</span> précis n'a été établi étant 
donné que c'est notre tout premier projet web.<br/>De même, la programmation orientée objet en PHP aurait permis la conception
d'un code plus clair et mieux ordonné, en instaurant par exemple une classe Membre qui créerait un objet par membre, auquel on pourrait 
appliquer toute une série de fonctions d'affichage et de modification (getter et setter), ce qui aurait évité la répétition de codes similaires.</p>

<p>Enfin, toute faille type injection de requête SQL, PHP ou autre est <span class="souligne">a priori</span> impossible, le détournement de l'URL du site 
non plus et quelques petits contrôles dans les formulaires ont été mis en place.</p>


<p><br/><strong>Pour conclure,</strong> la relative ampleur du projet nous a permis de constater l'importance de coder proprement, de bien organiser ses fichiers 
et de commenter le plus possible son code.<br/>Nous avons réalisé le niveau de complexité que peut représenter la conception d'un site professionnel 
et les angoisses que peuvent provoquer les éventuelles failles de sécurité.</p>

<p><br/>Concernant les droits, cet espace est privé, il est hébergé sur un serveur local et ne sera jamais mis en ligne.</p>

