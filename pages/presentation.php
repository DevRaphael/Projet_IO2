<p align="center"><span class="page-titre"><strong>L'Etudiant Autonome</strong></span><br/><br/>
<img src="images/presentation.png" alt="" /><br/>
<span class="modif">Je ne veux plus être mal informé ! Je veux L'Etudiant Autonome !</span></p>

<p><br/><strong>L’Etudiant Autonome</strong>, journal d’information étudiant, a été fondé en 2005 par deux jeunes étudiants de l’université d’Orléans, Jérémi Chil et William Richemond.</p>
<p>Petit à petit le journal est passé d’une feuille en noir et blanc collée sur des panneaux d’affichage à une maquette qui évolue au gré des années universitaires et des étudiants qui rejoignent l’association.<br/>Aujourd’hui, L’Etudiant Autonome en Région Centre est bien implanté sur les universités d’Orléans et de Tours, où il est distribué à 10.000 exemplaires.</p>
<p>À la rentrée 2009, l’édition de Paris a été créée par deux compères de longue date des fondateurs du journal, Maxime du Teil et Grégory Salomonovitch. Le premier étudiant à l’université Paris 9 – Dauphine et le second à l’université Paris 10 – Nanterre, depuis devenu Paris Ouest.</p>
<p>Aujourd’hui, l’Etudiant Autonome Paris compte une trentaine de membres et est distribué dans près de 10 facs parisiennes chaque mois. De part le nombre de tirages et sa renommée, il est devenu aujourd’hui le premier journal étudiant parisien !</p>
<p>Son comité de rédaction est ouvert à tous et est entièrement composé d'étudiants de toutes facs d'Ile-de-France.</p>
<p>Le journal, de 16 à 24 pages selon les mois, comporte des rubriques diverses et variées : environnement, voyage, économie, société, sport, et bien sûr les pages culture.</p>

<p>En mars 2010, le site de l’Etudiant Autonome ré-ouvre ses portes pour le bonheur de tous les fans du journal : articles exclusifs, reportages et interviews vidéos (LEAtv) à venir !</p>
<p>Merci de nous suivre !</p>
